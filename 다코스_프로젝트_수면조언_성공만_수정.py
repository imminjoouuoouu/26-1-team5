def score_sleep_quality(answers: dict) -> int:
    total = sum(q["weights"][answers[q["id"]] - 1] for q in SLEEP_QUESTIONS)
    min_w = sum(min(q["weights"]) for q in SLEEP_QUESTIONS)
    max_w = sum(max(q["weights"]) for q in SLEEP_QUESTIONS)
    return int(round(np.clip(1 + 9 * (total - min_w) / (max_w - min_w), 1, 10)))


def score_stress(answers: dict) -> int:
    total = sum(q["weights"][answers[q["id"]] - 1] for q in STRESS_QUESTIONS)
    min_w = sum(min(q["weights"]) for q in STRESS_QUESTIONS)
    max_w = sum(max(q["weights"]) for q in STRESS_QUESTIONS)
    return int(round(np.clip(1 + 9 * (total - min_w) / (max_w - min_w), 1, 10)))


def collect_survey_cli(questions: list, category: str) -> dict:
    print(f"\n{'─'*55}")
    print(f"  📋 {category} 설문 (1~4 중 하나 입력)")
    print(f"{'─'*55}")
    answers = {}
    for q in questions:
        print(f"\n{q['id']}. {q['question']}")
        for opt in q['options']:
            print(f"     {opt}")
        while True:
            try:
                val = int(input("  → 입력 (1~4): ").strip())
                if 1 <= val <= 4:
                    answers[q['id']] = val
                    break
                print("  ⚠️  1~4 사이 숫자를 입력하세요.")
            except ValueError:
                print("  ⚠️  숫자만 입력하세요.")
    return answers


