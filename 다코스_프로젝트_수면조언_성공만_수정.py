def read_wearable_hr(filepath, source="samsung", date_col=None, hr_col=None):
    """
    웨어러블 CSV → 최근 7일 안정시 심박수 평균 추출

    source 별 기본 컬럼:
      samsung : start_time, heart_rate
      apple   : startDate,  value
      fitbit  : dateTime,   value
      custom  : date_col, hr_col 직접 지정
    """
    col_map = {
        "samsung": ("start_time", "heart_rate"),
        "apple"  : ("startDate",  "value"),
        "fitbit" : ("dateTime",   "value"),
        "custom" : (date_col or "date", hr_col or "heart_rate"),
    }
    dcol, hcol = col_map.get(source, col_map["custom"])

    try:
        wdf     = pd.read_csv(filepath, parse_dates=[dcol])
        cutoff  = wdf[dcol].max() - pd.Timedelta(days=7)
        recent  = wdf[wdf[dcol] >= cutoff]
        # 하위 25% = 안정시 심박수 추정
        thresh  = recent[hcol].quantile(0.25)
        resting = recent[recent[hcol] <= thresh][hcol]
        avg_hr  = int(resting.mean())
        print(f"✅ 웨어러블 연동 성공 — 안정시 심박수: {avg_hr} bpm (최근 7일)")
        return avg_hr
    except Exception as e:
        print(f"⚠️  웨어러블 CSV 읽기 실패: {e}\n    혈압 추정값을 대신 사용합니다.")
        return None

