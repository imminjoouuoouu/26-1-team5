def total_sleep_analysis(user_raw: dict) -> dict:
    # RF 예측
    user_df  = pd.DataFrame([user_raw])
    user_sc  = scaler.transform(user_df)
    disorder = model.predict(user_sc)[0]
    proba    = model.predict_proba(user_sc)[0]
    proba_dict = {c: f"{round(p*100, 1)}%" for c, p in zip(model.classes_, proba)}

    # 페르소나, KNN
    sleep_score  = user_raw['Quality of Sleep']
    stress_score = user_raw['Stress Level']
    persona      = assign_persona(disorder, stress_score, sleep_score)
    knn          = get_knn_targets(user_raw)

    # Gemini 의학 분석
    print("\n🤖 Gemini 분석 중...")
    gemini_data = gemini_medical_analysis(persona, user_raw, knn)

    return {
        "disorder":    disorder,
        "proba":       proba_dict,
        "persona":     persona,
        "knn":         knn,
        "gemini":      gemini_data,
    }


