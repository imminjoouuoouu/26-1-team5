# ── 1. 데이터 입력 함수 ─────────────────────────────────────────
def input_patient_data():
    print("=" * 55)
    print("   수면장애 환자 데이터 입력 시스템")
    print("=" * 55)
    print("※ 각 항목을 입력 후 Enter를 누르세요.")
    print("※ 모르는 항목은 Enter를 눌러 건너뛸 수 있습니다.\n")

    data = {}

    # ── 기본 정보 ──
    print("[ 기본 정보 ]")
    data['age']    = input("  연령 (예: 38): ").strip() or "미입력"
    gender_input   = input("  성별 (1=남성, 2=여성, 3=기타): ").strip()
    data['gender'] = {"1": "남성", "2": "여성", "3": "기타"}.get(gender_input, "미입력")
    job_map = {"1": "사무직", "2": "교대 근무직", "3": "자유직/재택", "4": "학생", "5": "기타"}
    job_input      = input("  직업 (1=사무직, 2=교대근무, 3=자유직/재택, 4=학생, 5=기타): ").strip()
    data['job']    = job_map.get(job_input, "미입력")

    # ── 수면 패턴 ──
    print("\n[ 수면 패턴 ]")
    data['bedtime']      = input("  취침 시간 (예: 23:30): ").strip() or "미입력"
    data['waketime']     = input("  기상 시간 (예: 07:00): ").strip() or "미입력"
    data['sleep_onset']  = input("  입면 소요 시간 (분, 예: 60): ").strip() or "미입력"
    data['wakeup_count'] = input("  야간 각성 횟수 (회, 예: 2): ").strip() or "미입력"
    quality_map = {"1": "매우 나쁨", "2": "나쁨", "3": "보통", "4": "좋음", "5": "매우 좋음"}
    quality_input        = input("  수면의 질 (1=매우나쁨 ~ 5=매우좋음): ").strip()
    data['sleep_quality'] = quality_map.get(quality_input, "미입력")

    # ── 생활 습관 ──
    print("\n[ 생활 습관 ]")
    data['caffeine_cups']  = input("  일일 카페인 섭취량 (잔, 예: 2): ").strip() or "0"
    data['caffeine_time']  = input("  마지막 카페인 섭취 시간 (예: 15:00): ").strip() or "미입력"
    alcohol_map = {"1": "없음", "2": "주 1~2회", "3": "주 3~4회", "4": "매일"}
    alcohol_input          = input("  알코올 섭취 빈도 (1=없음, 2=주1~2회, 3=주3~4회, 4=매일): ").strip()
    data['alcohol']        = alcohol_map.get(alcohol_input, "미입력")
    data['phone_minutes']  = input("  취침 전 스마트폰 사용 시간 (분, 예: 40): ").strip() or "미입력"
    exercise_map = {"1": "없음", "2": "주 1~2회", "3": "주 3~4회", "4": "매일"}
    exercise_input         = input("  운동 빈도 (1=없음, 2=주1~2회, 3=주3~4회, 4=매일): ").strip()
    data['exercise']       = exercise_map.get(exercise_input, "미입력")

    # ── 증상 및 심리 상태 ──
    print("\n[ 증상 및 심리 상태 ]")
    data['stress_level'] = input("  스트레스 수준 (1~10, 예: 7): ").strip() or "미입력"

    symptom_map = {
        "1": "입면 어려움",
        "2": "중간에 자주 깸",
        "3": "이른 기상",
        "4": "주간 졸음",
        "5": "만성 피로",
        "6": "야간 불안/잡생각",
        "7": "기상 후 피로감",
        "8": "두통",
    }
    print("  주요 증상 (쉼표로 구분, 예: 1,4,5)")
    print("  1=입면어려움  2=중간에깸  3=이른기상  4=주간졸음")
    print("  5=만성피로    6=야간불안  7=기상후피로  8=두통")
    symptom_input  = input("  선택: ").strip()
    selected_nums  = [s.strip() for s in symptom_input.split(",") if s.strip()]
    data['symptoms'] = ", ".join(symptom_map[n] for n in selected_nums if n in symptom_map) or "미입력"

    data['extra'] = input("  기타 특이사항 (복용 약물, 기저질환 등): ").strip() or "없음"

    return data


