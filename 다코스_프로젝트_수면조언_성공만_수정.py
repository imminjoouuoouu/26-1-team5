def score_sleep_quality(answers: dict) -> tuple:
    """
    수면 질 설문 응답 → 1~10 점수 변환 (PSQI 가중치 기반)

    Parameters
    ----------
    answers : {"SQ1": 1~4, "SQ2": 1~4, ...}

    Returns
    -------
    (score: int, detail: str)
    """
    total = 0
    max_possible = 0
    for q in SLEEP_QUESTIONS:
        ans_idx = answers[q["id"]] - 1          # 1~4 → 0~3 인덱스
        total        += q["weights"][ans_idx]
        max_possible += max(q["weights"])

    # 가중치 합을 1~10 구간으로 선형 변환
    min_possible = sum(min(q["weights"]) for q in SLEEP_QUESTIONS)
    score = 1 + 9 * (total - min_possible) / (max_possible - min_possible)
    score = int(round(np.clip(score, 1, 10)))

    # 등급 설명
    if score >= 8:
        detail = "우수 — PSQI 기준 매우 양호한 수면 상태"
    elif score >= 6:
        detail = "양호 — 경미한 수면 문제 존재, 개선 여지 있음"
    elif score >= 4:
        detail = "주의 — 임상적 수면 장애 경계 수준"
    else:
        detail = "위험 — 전문의 상담 권장"

    return score, detail


def score_stress(answers: dict) -> tuple:
    """
    스트레스 설문 응답 → 1~10 점수 변환 (PSS 가중치 기반)

    Parameters
    ----------
    answers : {"SS1": 1~4, "SS2": 1~4, ...}

    Returns
    -------
    (score: int, detail: str)
    """
    total = 0
    max_possible = 0
    for q in STRESS_QUESTIONS:
        ans_idx = answers[q["id"]] - 1
        total        += q["weights"][ans_idx]
        max_possible += max(q["weights"])

    min_possible = sum(min(q["weights"]) for q in STRESS_QUESTIONS)
    score = 1 + 9 * (total - min_possible) / (max_possible - min_possible)
    score = int(round(np.clip(score, 1, 10)))

    if score <= 3:
        detail = "낮음 — 심리적 안정 상태"
    elif score <= 5:
        detail = "보통 — 일상적 스트레스 수준"
    elif score <= 7:
        detail = "높음 — 코르티솔 과다 분비 가능성"
    else:
        detail = "매우 높음 — 번아웃·불면 위험, 즉각적 관리 필요"

    return score, detail


def collect_survey_cli(questions: list, category: str) -> dict:
    """CLI 방식으로 설문 응답 수집"""
    print(f"\n{'─'*55}")
    print(f"  📋 {category} 설문 (1~4 중 하나 입력)")
    print(f"{'─'*55}")
    answers = {}
    for q in questions:
        print(f"\n{q['id']}. {q['question']}")
        for opt in q['options']:
            print(f"     {opt}")
        while True:
            try:
                val = int(input("  → 입력 (1~4): ").strip())
                if 1 <= val <= 4:
                    answers[q['id']] = val
                    break
                print("  ⚠️  1~4 사이 숫자를 입력하세요.")
            except ValueError:
                print("  ⚠️  숫자만 입력하세요.")
    return answers


